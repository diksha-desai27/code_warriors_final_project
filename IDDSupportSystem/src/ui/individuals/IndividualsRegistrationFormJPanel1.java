    public Boolean calculateAge() {
        LocalDate dob = LocalDate.parse(individual.getBirthDate());
        LocalDate curDate = LocalDate.now();
        if (Period.between(dob, curDate).getYears() >= 18) {
            return true;
        }
        return false;
    }

